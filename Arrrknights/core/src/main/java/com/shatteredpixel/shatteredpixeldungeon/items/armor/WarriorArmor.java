public class WarriorArmor extends ClassArmor {

	{
		image = ItemSpriteSheet.ARMOR_WARRIOR;
	}

	@Override
	public void doSpecial() {

		if (charge < 35) {
			GLog.w(Messages.get(this, "low_charge"));}
		else {
			charge -= 35;
			updateQuickslot();

			Buff.affect(curUser, SeethingBurst.class, 5);

			curUser.busy();
			curUser.spendAndNext(1);
		}
	};
}